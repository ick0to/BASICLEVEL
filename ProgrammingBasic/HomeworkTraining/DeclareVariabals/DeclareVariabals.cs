using System;

namespace DeclareVariabals
{
    class DeclareVariabals
    {
        static void Main(string[] args)
        {
            byte numberByte = 97;
            sbyte numberSbyte = -115;
            ushort numberUshort = 52130;
            int numberInt = -10000;
            uint numberUint = 4825932;

            Console.WriteLine("byte = " + numberByte);
            Console.WriteLine("sbyte = " + numberSbyte);
            Console.WriteLine("ushort = " + numberUshort);
            Console.WriteLine("int = " + numberInt);
            Console.WriteLine("uint = " + numberUint);

        }
    }
}
